# Simple assignment
balance = 500
print(balance)  # 500

# Assignment with addition
balance = 500
balance += 200
print(balance)  # 700

# Assignment with subtraction
balance = 500
balance -= 200
print(balance)  # 300

# Assignment with multiplication
balance = 500
balance *= 200
print(balance)  # 100000

# Assignment with division
balance = 500
balance /= 200
print(balance)  # 2.5

# Assignment with exponentiation
balance = 500
balance **= 2
print(balance)  # 250000

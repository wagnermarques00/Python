balance = 450
withdraw = 200

# Equal
print(balance == withdraw)  # False

# Not equal
print(balance != withdraw)  # True

# Greater than
print(balance > withdraw)  # True

# Less than
print(balance < withdraw)  # False

# Greater than or equal to
print(balance >= withdraw)  # True

# Less than or equal to
print(balance <= withdraw)  # False

# Addition
print(10 + 3)  # 13

# Subtraction
print(10 - 3)  # 7

# Multiplication
print(10 * 3)  # 30

# Division
print(10 / 3)  # 3.333333333333333

# Floor division
print(10 // 3)  # 3

# Modulus
print(10 % 3)  # 1

# Exponentiation
print(10**3)  # 1000

# Examples of order of precedence of arithmetic operations
print(10 - 5 * 2)  # 0
print((10 - 5) * 2)  # 10
print(10**2 * 2)  # 200
print(10 / 2 * 4)  # 20.0

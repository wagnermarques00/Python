# Creating a dictionary named 'person' with initial key:value pairs.
person = {"name": "Wagner", "age": 32, "phone": "1234-5678"}

# Accessing and printing information from the 'person' dictionary.
print(person["name"])  # Wagner
print(person["age"])  # 32
print(person["phone"])  # 1234-5678

# Modifying values in the 'person' dictionary.
person["name"] = "Joseph"
person["age"] = 66
person["phone"] = "8765-4321"

# Printing the modified 'person' dictionary.
print(person)  # {'name': 'Joseph', 'age': 66, 'phone': '8765-4321'}

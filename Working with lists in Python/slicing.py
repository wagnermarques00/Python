slicing_list = ["p", "y", "t", "o", "n"]

print(slicing_list[2:])  # ['t', 'o', 'n']
print(slicing_list[:2])  # ['p', 'y']
print(slicing_list[1:3])  # ['y', 't']
print(slicing_list[0:3:2])  # ['p', 't']
print(slicing_list[::])  # ['p', 'y', 't', 'o', 'n']
print(slicing_list[::-1])  # ['n', 'o', 't', 'y', 'p']
